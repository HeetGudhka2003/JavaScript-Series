// String to number conversion

console.log("1" + 2); // 12

console.log(1 + "2"); // 12

console.log("1" + 2 + 2); // 122

console.log(1 + 2 + "2"); // 32

// To implement certain operations first we should use parenthesis

console.log((3 + 4) * 5 % 3); // 2

// if the string is given first, all of them will be considered as a string and if the string is in the last position then the operations will take place first.

console.log(true); // true

